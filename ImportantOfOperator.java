public class ImportantOfOperator {
  public static void main(String[] args) {
    float x = 5+8*9; // 8*9=72+5=77
    System.out.println(x);
    float y = 10-4+2; // 10-4=6+2=8
    System.out.println(y);
    float z = 10-(2+1); // 2+1=3 10-3=7
    System.out.println(z);
    float a = 5*2-40/5; // 5*2=10 40/5=8 10-8=2
    System.out.println(a);
    float b = 7+8/2+25; //8/2=4 7+4+25=36
    System.out.println(b);
  }
}
